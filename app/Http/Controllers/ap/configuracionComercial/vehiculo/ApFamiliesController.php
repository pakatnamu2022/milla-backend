<?php

namespace App\Http\Controllers\ap\configuracionComercial\vehiculo;

use App\Http\Controllers\Controller;
use App\Http\Requests\ap\configuracionComercial\vehiculo\IndexApFamiliesRequest;
use App\Http\Requests\ap\configuracionComercial\vehiculo\StoreApFamiliesRequest;
use App\Http\Requests\ap\configuracionComercial\vehiculo\UpdateApFamiliesRequest;
use App\Http\Services\ap\configuracionComercial\vehiculo\ApFamiliesService;

class ApFamiliesController extends Controller
{
  protected ApFamiliesService $service;

  public function __construct(ApFamiliesService $service)
  {
    $this->service = $service;
  }

  public function index(IndexApFamiliesRequest $request)
  {
    try {
      return $this->service->list($request);
    } catch (\Throwable $th) {
      return $this->error($th->getMessage());
    }
  }

  public function store(StoreApFamiliesRequest $request)
  {
    try {
      return $this->success($this->service->store($request->validated()));
    } catch (\Throwable $th) {
      return $this->error($th->getMessage());
    }
  }

  public function show($id)
  {
    try {
      return $this->success($this->service->show($id));
    } catch (\Throwable $th) {
      return $this->error($th->getMessage());
    }
  }

  public function update(UpdateApFamiliesRequest $request, $id)
  {
    try {
      $data = $request->validated();
      $data['id'] = $id;
      return $this->success($this->service->update($data));
    } catch (\Throwable $th) {
      return $this->error($th->getMessage());
    }
  }

  public function destroy($id)
  {
    try {
      return $this->service->destroy($id);
    } catch (\Throwable $th) {
      return $this->error($th->getMessage());
    }
  }
}
